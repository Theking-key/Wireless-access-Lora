#include "cmd_queue.h"

#define CMD_HEAD 0XEE                                                  //ﾖ｡ﾍｷ
#define CMD_TAIL 0XFFFCFFFF                                            //ﾖ｡ﾎｲ

typedef struct _QUEUE                                             
{                                                                 
    qsize _head;                                                       //魹ｽ魹ｽ魹ｽ魹ｽﾍｷ
    qsize _tail;                                                       //魹ｽ魹ｽ魹ｽ魹ｽﾎｲ
    qdata _data[QUEUE_MAX_SIZE];                                       //魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾝｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
}QUEUE;                                                           

static QUEUE que = {0,0,0};                                            //ﾖｸ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
static uint32 cmd_state = 0;                                           //魹ｽ魹ｽ魹ｽ魹ｽﾖ｡ﾎｲ魹ｽ魹ｽ魹ｽﾗｴﾌｬ
static qsize cmd_pos = 0;                                              //魹ｽ魹ｽﾇｰﾖｸ魹ｽ魹ｽﾖｸ魹ｽ魹ｽﾎｻ魹ｽ魹ｽ

/*! 
*  ¥brief  魹ｽ魹ｽ魹ｽﾖｸ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
*/
void queue_reset()
{
    que._head = que._tail = 0;
    cmd_pos = cmd_state = 0;
}
/*! 
* ¥brief  魹ｽ魹ｽ魹ｽ魹ｽﾖｸ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
* ¥detial 魹ｽ魹ｽ魹ｽﾚｽ魹ｽ魹ｽﾕｵ魹ｽ魹ｽ魹ｽ魹ｽﾝ｣魹ｽﾍｨ魹ｽ魹ｽ魹ｽﾋｺ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｸ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 
*  ¥param  _data ﾖｸ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
*/
void queue_push(qdata _data)
{
    qsize pos = (que._head+1)%QUEUE_MAX_SIZE;
    if(pos!=que._tail)                                                //魹ｽ魹ｽ魹ｽ魹ｽﾗｴﾌｬ
    {
        que._data[que._head] = _data;
        que._head = pos;
    }
}

//魹ｽﾓｶ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾈ｡ﾒｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
static void queue_pop(qdata* _data)
{
    if(que._tail!=que._head)                                          //魹ｽﾇｿ魹ｽﾗｴﾌｬ
    {
        *_data = que._data[que._tail];
        que._tail = (que._tail+1)%QUEUE_MAX_SIZE;
    }
}

//魹ｽ魹ｽﾈ｡魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾐｧ魹ｽ魹ｽ魹ｽﾝｸ魹ｽ魹ｽ魹ｽ
static qsize queue_size()
{
    return ((que._head+QUEUE_MAX_SIZE-que._tail)%QUEUE_MAX_SIZE);
}
/*! 
*  ¥brief  魹ｽ魹ｽﾖｸ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾈ｡魹ｽ魹ｽﾒｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｸ魹ｽ魹ｽ
*  ¥param  cmd ﾖｸ魹ｽ魹ｽ魹ｽ魹ｽﾕｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
*  ¥param  buf_len ﾖｸ魹ｽ魹ｽ魹ｽ魹ｽﾕｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾐ｡
*  ¥return  ﾖｸ魹ｽ讝､魹ｽﾈ｣魹ｽ0魹ｽ魹ｽﾊｾ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｸ魹ｽ魹ｽ
*/
qsize queue_find_cmd(qdata *buffer,qsize buf_len)
{
    qsize cmd_size = 0;
    qdata _data = 0;

    while(queue_size()>0)
    {
        //ﾈ｡ﾒｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
        queue_pop(&_data);

        if(cmd_pos==0&&_data!=CMD_HEAD)                               //ﾖｸ魹ｽ魹ｽ魹ｽﾒｻ魹ｽ魹ｽ魹ｽﾖｽﾚｱ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖ｡ﾍｷ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
        {
            continue;
        }
        //    LED2_ON;
        if(cmd_pos<buf_len)                                           //魹ｽ魹ｽﾖｹ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
            buffer[cmd_pos++] = _data;

        cmd_state = ((cmd_state<<8)|_data);                           //ﾆｴ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ4魹ｽ魹ｽ魹ｽﾖｽﾚ｣魹ｽ魹ｽ魹ｽ魹ｽﾒｻ魹ｽ魹ｽ32ﾎｻ魹ｽ魹ｽ魹ｽ魹ｽ

        //魹ｽ魹ｽ魹ｽ4魹ｽ魹ｽ魹ｽﾖｽ魹ｽ魹ｽ魹ｽﾖ｡ﾎｲﾆ･魹ｽ茱ｬ魹ｽﾃｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖ｡
        if(cmd_state==CMD_TAIL)
        {
            //LED2_ON;
            cmd_size = cmd_pos;                                       //ﾖｸ魹ｽ魹ｽ魹ｽﾖｽﾚｳ魹ｽ魹ｽ魹ｽ
            cmd_state = 0;                                            //魹ｽ魹ｽ魹ｽﾂｼ魹ｽ魹ｽﾖ｡ﾎｲ魹ｽ魹ｽ
            cmd_pos = 0;                                              //魹ｽ魹ｽﾎｻﾖｸ魹ｽ魹ｽﾖｸ魹ｽ魹ｽ

#if(CRC16_ENABLE)
            //ﾈ･魹ｽ魹ｽﾖｸ魹ｽ魹ｽﾍｷﾎｲEE魹ｽ魹ｽﾎｲFFFCFFFF魹ｽ魹ｽ魹ｽ魹ｽ5魹ｽ魹ｽ魹ｽﾖｽﾚ｣魹ｽﾖｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾝｲ魹ｽ魹ｽ魹ｽCRC
            if(!CheckCRC16(buffer+1,cmd_size-5))                      //CRCﾐ｣魹ｽ魹ｽ
                return 0;

            cmd_size -= 2;                                            //ﾈ･魹ｽ魹ｽCRC16魹ｽ魹ｽ2魹ｽﾖｽﾚ｣魹ｽ
#endif
            return cmd_size;
        }
    }
    return 0;                                                         //ﾃｻ魹ｽ魹ｽ魹ｽﾎｳ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾒｻﾖ｡
}